                <h3>Le patois ou patwa</h3>
                <p>Les Rastas utilisent un patois légèrement différent du patois Jamaïcain : Le Dread Talk ou Patois Rasta. <br> Le patois Jamaïcain découle des racines Africaines alors que le dialecte Rasta est un jargon plus spécifique. <br> Ce jargon Rasta est un mélange vivant de mots Créoles et de dialectes Africains, de métaphores Bibliques, d'inventions sémantiques, de formules argotiques. Cette mosaïque donne naissance à une langue originale qui se retrouve, entre autres dans toutes les chansons de Reggae qui est le style musical dominant sur l’île.</p>
                <p> Une des particularités principales du patois Rasta est l’inversion pronominale entre le nominatif et l’accusatif : Le "I" Anglais devient "Me" et inversement. Une autre caractéristique du patois Rasta est que les verbes sautent souvent dans les phrases (ellipse) e.g. "you a liar, you a thief " et les raccourcis Anglais usuels sont encore plus raccourcis ("want to" exprimé "wanna" en anglais parlé, donne "waan" en patois Rasta). <br> Les Rastas modifient également les propositions Britanniques. Ils utilisent par exemple la préposition "a", issue de la langue Espagnole, plutôt que le "to" Anglais. Exemple "go a shop".</p>

