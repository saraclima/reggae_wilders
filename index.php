	            <p> Genre musical né en Jamaïque dans les années 1960, le reggae est le fruit d'un vaste métissage. La plus populaire des musiques jamaïcaines ne cesse d'évoluer et d'étendre son influence a travers le monde.</p>
	            
	            <h3>Des origines métissées</h3>
	            <p>Le reggae apparu en Jamaïque à la fin des années 1960 est le résultat de nombreuses rencontres et de métissages. Il dérive de musiques jamaïcaines comme le Ska et le Rocksteady mais plonge ses racines dans les musiques traditionnelles caribéennes telles que le Mento et le Calypso et les musiques africaines importées en Jamaïque par les esclaves. <br>Ce style musical a aussi été influencé par la musique américaine de type Rythm'n blues, Jazz et Soul music, alors très en vogue sur l'île.</p>
	            
	            <h3>Une étymologie incertaine</h3>
	            <p>L'étymologie du mot "reggae" est incertaine. Il dériverait de l'anglais regular people ("gens du peuple") ou de raggedy ("déguenillé"). D'autres sources indiquent qu'il vient de l'argot jamaïcain streggae ("femme facile"). <br> Ce qui est sûr, c'est que le terme de reggae a été utilisé pour la première fois dans la chanson Do the reggae écrite en 1968 par Toots, leader du groupe The Maytals. La petite vidéo ci-dessous vous permettra de découvrir cette chanson considérée comme l'acte de naissance de ce genre musical.</p>

